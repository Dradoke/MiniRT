#include "libft.h"

// Deletes and frees an entire linked list.
// Applies 'del' to each node's content and frees all nodes.
// Sets the list pointer to NULL after clearing.
void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		temp = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = temp;
	}
}
