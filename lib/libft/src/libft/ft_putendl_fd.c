#include "libft.h"

// Outputs a string followed by a newline to a file descriptor.
// Writes string 's' followed by '\n' to file descriptor 'fd'.
// Does nothing if 's' is NULL.
void	ft_putendl_fd(char *s, int fd)
{
	if (!s)
		return ;
	ft_putstr_fd(s, fd);
	ft_putchar_fd('\n', fd);
}
