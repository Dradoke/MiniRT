#include "libft.h"

// Converts a lowercase letter to uppercase.
// Changes a character from a-z to A-Z.
// Returns the uppercase equivalent or the original character.
int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		return (c - 32);
	return (c);
}
