#include "libft.h"

// Fills memory with a specified byte.
// Sets 'len' bytes of memory starting at 'b' to the value 'c'.
// Returns a pointer to the memory area 'b'.
void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*str;
	size_t			i;

	str = b;
	i = 0;
	while (i < len)
		*(str + i++) = (unsigned char)c;
	return (b);
}
