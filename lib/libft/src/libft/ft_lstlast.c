#include "libft.h"

// Finds the last node of a linked list.
// Iterates through the list until finding the last node.
// Returns the last node or NULL if the list is empty.
t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst->next)
		lst = lst->next;
	return (lst);
}
